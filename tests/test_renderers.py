class TestAgentRenderer:
    """Test suite for agent file rendering."""

    def test_render_basic_agent(self) -> None:
        """Test rendering an agent with minimal configuration."""
        agent = AgentConfig(
            slug="test-agent",
            display_name="Test Agent",
            description="A test agent",
            prompt_file="agents/test.md",
            prompt_text="You are a test agent.",
            readonly=False,
            tools=["search/codebase", "edit"],
            models=AgentModels(
                primary="gpt-4", kilo_id="kilo/gpt-4", vscode_id="gpt-4"
            ),
            handoffs=[],
            kilo=AgentKiloConfig(color="gray"),
        )

        frontmatter_fields = ["name", "description", "tools"]
        context = {}
        result = render_agent_file(
            agent, "gpt-4", frontmatter_fields, context, "vscode"
        )

        assert "name: Test Agent" in result
        assert "description: A test agent" in result
        assert "- search/codebase" in result
        assert "- edit" in result
        assert "You are a test agent." in result
        assert "Generated by project-flow v0.1.0" in result

    def test_render_agent_with_handoffs(self) -> None:
        """Test rendering agent with handoff configuration."""
        agent = AgentConfig(
            slug="agent-a",
            display_name="Agent A",
            description="Agent with handoffs",
            prompt_file="agents/a.md",
            prompt_text="You are Agent A.",
            readonly=False,
            tools=["search/codebase"],
            models=AgentModels(
                primary="gpt-4", kilo_id="kilo/gpt-4", vscode_id="gpt-4"
            ),
            handoffs=[
                AgentHandoff(
                    label="Send to B", agent_slug="agent-b", send_immediately=False
                ),
                AgentHandoff(
                    label="Urgent to C", agent_slug="agent-c", send_immediately=True
                ),
            ],
            kilo=AgentKiloConfig(color="blue"),
        )

        frontmatter_fields = ["name", "description", "tools", "handoffs"]
        context = {
            "AVAILABLE_AGENTS": [
                {"slug": "agent-a", "display_name": "Agent A", "description": ""},
                {"slug": "agent-b", "display_name": "Agent B", "description": ""},
                {"slug": "agent-c", "display_name": "Agent C", "description": ""},
            ]
        }
        result = render_agent_file(
            agent, "gpt-4", frontmatter_fields, context, "vscode"
        )

        assert "handoffs:" in result
        assert "- label: Send to B" in result
        assert "agent: Agent B" in result
        assert "send: false" in result
        assert "- label: Urgent to C" in result
        assert "agent: Agent C" in result
        assert "send: true" in result

