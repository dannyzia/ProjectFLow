class TestSkillsRenderer:
    """Test suite for skill file rendering."""

    def test_render_skill_file(self) -> None:
        """Test rendering a single skill file."""
        skill = {
            "name": "testing",
            "description": "Testing practices",
            "content": "# Testing Skill\n\nWrite comprehensive tests.",
        }

        result = render_skill_file(skill)

        assert "# Testing Skill" in result
        assert "Write comprehensive tests." in result
        assert "Generated by project-flow" in result

    def test_render_skills_with_jinja2_substitution(self) -> None:
        """Test that skill content undergoes template substitution."""
        skills_config = [
            SkillConfig(
                name="documentation",
                description="Doc standards",
                content="# Documentation\n\nYou are documenting {{ PROJECT_NAME }}.",
            )
        ]
        context = {"PROJECT_NAME": "My App"}

        result = render_skills(skills_config, context)

        assert len(result) == 1
        assert result[0]["name"] == "documentation"
        assert "You are documenting My App." in result[0]["content"]

    def test_render_multiple_skills(self) -> None:
        """Test rendering multiple skill configurations."""
        skills_config = [
            SkillConfig(
                name="testing",
                description="Test practices",
                content="# Testing\n\nTest everything.",
            ),
            SkillConfig(
                name="documentation",
                description="Doc standards",
                content="# Documentation\n\nDocument everything.",
            ),
        ]
        context = {}

        result = render_skills(skills_config, context)

        assert len(result) == 2
        assert result[0]["name"] == "testing"
        assert result[1]["name"] == "documentation"
        assert "# Testing" in result[0]["content"]
        assert "# Documentation" in result[1]["content"]

    def test_render_skills_empty_list(self) -> None:
        """Test rendering with no skills configured."""
        skills_config = []
        context = {}

        result = render_skills(skills_config, context)

        assert result == []

    def test_render_skill_with_multiline_content(self) -> None:
        """Test rendering skill with complex multiline content."""
        skill = {
            "name": "complex",
            "description": "Complex skill",
            "content": """# Complex Skill

## Guidelines

1. First rule
2. Second rule
3. Third rule

### Subsection
More details here.
""",
        }

        result = render_skill_file(skill)

        assert "# Complex Skill" in result
        assert "## Guidelines" in result
        assert "1. First rule" in result
        assert "### Subsection" in result
        assert "Generated by project-flow" in result

    def test_render_skill_preserves_yaml_special_chars(self) -> None:
        """Test that skills with YAML special characters render correctly."""
        skill = {
            "name": "yaml-safe",
            "description": "Skill with special chars",
            "content": """# YAML Safe Skill

- List item 1
- List item 2
- List with: colons

Value: 123
Nested:
  key: value
""",
        }

        result = render_skill_file(skill)

        assert "- List item 1" in result
        assert "- List with: colons" in result
        assert "Value: 123" in result
        assert "key: value" in result
